//Task 5 switch case to assign gread based on A B C D F

let score = 80;

switch (true) {
  case score >= 90: {
    console.log("Grade: A");
    break;
  }
  case score >= 70: {
    console.log("Grade: B");
    break;
  }
  case score >= 60: {
    console.log("Grade: C");
    break;
  }
  case score >= 50: {
    console.log("Grade: D");
    break;
  }
  case score <= 40: {
    console.log("Grade: F");
    break;
  }
  default: {
    console.log("Invalid Score");
    break;
  }
}
